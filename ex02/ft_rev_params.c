#include <unistd.h>
#include <stdio.h>

void	ft_putstr(char *str);

int	main(int argc, char *argv[])
{
	int	i;

	i = (argc - 1);
	while (i != 0)
	{
		ft_putstr(argv[i]);
		i--;
	}
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
